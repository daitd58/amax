	function om_img_any_resize($att_id, $thumb_size='full', $crop = null, $single=true) {
		global $_wp_additional_image_sizes;
		global $_OM_THEME_THUMBNAIL_SIZES;
		
		$no_resize=false;
		
		if(( isset( $_wp_additional_image_sizes[$thumb_size] ) && is_array( $_wp_additional_image_sizes[$thumb_size] ) ) || in_array( $thumb_size, array( 'thumbnail', 'thumb', 'medium', 'large', 'full' ) )) {
			
			$src=wp_get_attachment_image_src( $att_id, $thumb_size );
			if($single) {
				return $src[0];
			} else {
				return $src;
			}
			
		} elseif(isset($_OM_THEME_THUMBNAIL_SIZES[$thumb_size])) {
			
			$src=wp_get_attachment_image_src( $att_id, 'full' );
			return om_img_resize($src[0], $thumb_size, $single);
			
		} elseif(preg_match_all( '/\d+/', $thumb_size, $thumb_matches )) {

			if ( isset( $thumb_matches[0] ) ) {
				$width=$height=false;
				if ( count( $thumb_matches[0] ) > 1 ) {
					$width = $thumb_matches[0][0];
					$height = $thumb_matches[0][1];
				} elseif ( count( $thumb_matches[0] ) == 1 ) {
					$width = $height = $thumb_matches[0][0];
				}
				if($width && $height) {
					$src=wp_get_attachment_image_src( $att_id, 'full' );
					return om_img_custom_resize($src[0], $width, $height, $crop, $single);
				} else {
					$no_resize=true;
				}
			}

		} else {
			$no_resize=true;
		}
			
		if($no_resize) {
			if($single) {
				$src=wp_get_attachment_image_src( $att_id, 'full' );
				if($src) {
					return $src[0];
				} else {
				 	return false;
				}
			} else {
				return false;
			}
		}
		
	}
